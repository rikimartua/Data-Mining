% --- Executes on button press in pushbutton2.
function pushbutton2_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton2 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
t = xlsread(get(handles.text2,'string')); %ambil = xlsread(get(t));

a = str2num(get(handles.edit3,'string'));%Nilai age%
b = str2num(get(handles.edit4,'string'));%Nilai prescription%
c = str2num(get(handles.edit5,'string'));%Nilai astigmatic%
d = str2num(get(handles.edit6,'string'));%Nilai tier prosuction rate%

n = str2num(get(handles.edit12,'string'));

h = xlsread(get(handles.text12,'string')); %ambill = xlsread(get(g));

sampel = [a , b, c, d];
class = fitcknn(t ,h ,'NumNeighbors', n); %class = fitcknn(t , g ,'NumNeighbors', k);
klasifikasi=predict(class,sampel);
hasil=num2str(klasifikasi);
if(klasifikasi == 1)
    hasil1='1 : the patient should be fitted with hard contact lenses';
elseif (klasifikasi == 2)
    hasil1='2 : the patient should be fitted with soft contact lenses';
elseif (klasifikasi == 3)
    hasil1='3 : the patient should not be fitted with contact lenses';
end
set(handles.text15,'string',(hasil1));   
set(handles.edit13,'string',(hasil));

